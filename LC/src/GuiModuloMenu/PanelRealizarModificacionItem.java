    private void jlModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlModificarMouseClicked
        
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("LCPU"); //LCPU es el nombre de nuestra unidad de persistencia
        ItemJpaController dao = new ItemJpaController(emf);
        
        Item item = dao.findItem(Integer.parseInt(jtfID.getText()));
        
        //item.setIdItem(Integer.parseInt(jtfID.getText()));
        item.setNombre(jtfNombre.getText());
        item.setPrecio(Integer.parseInt(jtfPrecio.getText()));
        item.setCategoria(jcbCategoria.getSelectedItem().toString());
        item.setDescripcion(jtaDescripcion.getText());
        
        try {
            dao.edit(item);
            JOptionPane.showMessageDialog(null, "Item modificado exitosamente.");
            jtfID.setText("");
            jtfNombre.setText("");
            jtfPrecio.setText("");
            jcbCategoria.setSelectedIndex(0);
            jtaDescripcion.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Este Item no existe.");
            jtfID.setText("");
            jtfNombre.setText("");
            jtfPrecio.setText("");
            jcbCategoria.setSelectedIndex(0);
            jtaDescripcion.setText("");
        }finally{
            emf.close();
        }
    }//GEN-LAST:event_jlModificarMouseClicked


